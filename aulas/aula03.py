"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"')

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'")

# Escape
print("Luiz \"Otávio\"")
"""Esse caracter de escape é a barra invertida, ou seja ele pula o próximo caracter depois da barra invertida"""

# r - mostra tudo mesmooo que esta ali dentro das aspas em vermelho, até o caracter de escape
print(r"Luiz \"Otávio\"")